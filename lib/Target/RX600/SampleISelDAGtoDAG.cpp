// RX600DAGToDAGISel - RX600 specific code to select RX600 machine
// instructions for SelectionDAG operations.
//===----------------------------------------------------------------------===//
namespace {

class RX600DAGToDAGISel : public SelectionDAGISel {

  /// TM - Keep a reference to RX600TargetMachine.
  const RX600TargetMachine &TM;

  /// Subtarget - Keep a pointer to the RX600Subtarget around so that we can
  /// make the right decision when generating code for different targets.
  const RX600Subtarget &Subtarget;

public:
  explicit RX600DAGToDAGISel(RX600TargetMachine &tm) :
  SelectionDAGISel(tm),
  TM(tm),
  Subtarget(tm.getSubtarget<RX600Subtarget>()) {}

  // Pass Name
  virtual const char *getPassName() const {
    return "RX600 DAG->DAG Pattern Instruction Selection";
  }

private:
  // Include the pieces autogenerated from the target description.
  #include "RX600GenDAGISel.inc"

  /// getTargetMachine - Return a reference to the TargetMachine, casted
  /// to the target-specific type.
  const RX600TargetMachine &getTargetMachine() {
    return static_cast<const RX600TargetMachine &>(TM);
  }

  /// getInstrInfo - Return a reference to the TargetInstrInfo, casted
  /// to the target-specific type.
  const RX600InstrInfo *getInstrInfo() {
    return getTargetMachine().getInstrInfo();
  }

  SDNode *Select(SDNode *N) /*override*/;

  // Complex Pattern.
  bool SelectAddr(SDValue N, SDValue &Base, SDValue &Offset);
};
}

/// ComplexPattern used on RX600InstrInfo
/// Used on RX600 Load/Store instructions
bool RX600DAGToDAGISel::
SelectAddr(SDValue N, SDValue &Base, SDValue &Offset) {
  EVT ValTy = N.getValueType();

  if (FrameIndexSDNode *FIN = dyn_cast<FrameIndexSDNode>(N)) {
    Base   = CurDAG->getTargetFrameIndex(FIN->getIndex(), ValTy);
    Offset = CurDAG->getTargetConstant(0, ValTy);
    return true;
  }

  llvm_unreachable("Unknown pattern");
  return true;
}

/// Select instructions not customized! Used for
/// expanded, promoted and normal instructions
SDNode* RX600DAGToDAGISel::
Select(SDNode *Node) {
  // Select the default instruction
  SDNode *ResNode = SelectCode(Node);

  DEBUG(errs() << "=> ");
  if (ResNode == NULL || ResNode == Node)
    DEBUG(Node->dumpr(CurDAG));
  else
    DEBUG(ResNode->dumpr(CurDAG));
  DEBUG(errs() << "\n");
  return ResNode;
}

/// createRX600ISelDag - This pass converts a legalized DAG into a
/// RX600-specific DAG, ready for instruction scheduling.
FunctionPass *llvm::createRX600ISelDag(RX600TargetMachine &TM) {
  return new RX600DAGToDAGISel(TM);
}
